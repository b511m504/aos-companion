import { execSync } from 'node:child_process'
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const __filename = fileURLToPath(import.meta.url)
const __dirname = dirname(__filename)
const root = resolve(__dirname, '..')

const pkg = JSON.parse(readFileSync(resolve(root, 'package.json'), 'utf8'))
const BUILD_TIME = new Date().toISOString()
const RUNTIME_MODE = process.env.RUNTIME_MODE || 'guided-runtime-refactor'

let GIT_HASH = 'nogit'
try {
  GIT_HASH = execSync('git rev-parse --short HEAD', { cwd: root, encoding: 'utf8' }).trim()
} catch {
  // no git available
}

function compactBuildStamp(iso) {
  return String(iso).slice(11, 16).replace(':', '')
}

const PACKAGE_VERSION = pkg.version || '0.0.0'
const DISPLAY_VERSION = `${PACKAGE_VERSION}-dev-${GIT_HASH}-${compactBuildStamp(BUILD_TIME)}`

const out = `// Auto-generated by scripts/write-build-info.mjs
export const BUILD_INFO = {
  appVersion: ${JSON.stringify(PACKAGE_VERSION)},
  packageVersion: ${JSON.stringify(PACKAGE_VERSION)},
  displayVersion: ${JSON.stringify(DISPLAY_VERSION)},
  buildTime: ${JSON.stringify(BUILD_TIME)},
  runtimeMode: ${JSON.stringify(RUNTIME_MODE)},
  gitHash: ${JSON.stringify(GIT_HASH)},
  generated: true,
}
`

mkdirSync(resolve(root, 'src'), { recursive: true })
writeFileSync(resolve(root, 'src/buildInfo.js'), out, 'utf8')
console.log('[build-info] generated', BUILD_TIME, GIT_HASH)

